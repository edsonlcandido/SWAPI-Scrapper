CREATE TABLE Spaceship (
    Id               INTEGER PRIMARY KEY,
    Name             VARCHAR,
    Model            VARCHAR,
    Manufacturer     VARCHAR,
    CostInCredits    VARCHAR,
    Length           TEXT,
    MaxSpeed         VARCHAR,
    Crew             VARCHAR,
    Passengers       VARCHAR,
    CargoCapacity    VARCHAR,
    Consumables      VARCHAR,
    Class            VARCHAR,
    HyperdriveRating VARCHAR,
    MGLT             VARCHAR
);